for i in range(1, 10):  # 10 is last-1 , Range is (start, last-1)
    print(i)
for i in range(0, 10, 2):  # 10 is last-1 , Range is (start, last-1)
    print(i)
for i in range(3, 5):  # 3,4
    print(i)
for i in range(1, 10, 2):  # 1 3 5 7 9
    print(i)
for i in range(10):  # 0-9
    print(i)
for i in range(-10, 10, 2):  # -10, -8 ... 8
    print(i)
for i in range(-1, -10, -1):  # -1,-2 ...-9
    print(i)
